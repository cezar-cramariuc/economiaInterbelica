    <h1>Jurnalul lui Harry Byers în URSS, 1930-1931</h1>
    <h2>Descriere</h2>
    <p>Harry Byers a fost un bărbat american care a lucrat pentru Trustul de Stat al Cerealelor din URSS (acum Rusia și alte 11 țări). A călătorit în URSS pentru a-i învăța pe muncitorii sovietici cum să opereze mașini industriale mari ca parte a primului plan cincinal. Harry Byers a sosit în URSS la scurt timp după începerea planului, iar în această sursă descrie observațiile sale despre viața în URSS în cadrul acestui plan. A locuit în mai multe orașe, inclusiv Moscova, Groznîi și Ussuriysk, care era aproape de granița cu China.</p>
