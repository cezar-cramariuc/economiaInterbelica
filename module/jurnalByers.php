            1 septembrie 1930
            <p>
                Oamenii își ucid copiii, pentru că nu au ce să mănânce.
                16 septembrie 1930
                [...] Piața chineză se închide. Arată rău, unde vom mânca? Oh, mi-aș dori să fim acasă.
            </p>
            8 octombrie 1930
            <p>[...] am primit 1/4 lb de brânză. 1,85 dolari. Ne înfometăm încet stomacul și am primit prăjituri în valoare de 30 de dolari primăvara trecută. Toate au dispărut.</p>
            14 noiembrie 1930
            <p>Cât timp mai putem rezista, nimeni nu știe. Mi s-a ordonat să merg într-un alt sat. Știu că vom muri de foame. Piața vinde lapte acru.</p>
            19 decembrie 1930
            <p>Bătrânul vine acasă în fiecare zi să ne spună că nu există mâncare. fermă colectivă aici toți bărbații au trecut în China. Toți instructorii plănuiesc evadarea.</p>
            3 iunie 1931
            <p>Oamenii au venit cu cuțite din sat.</p>
            13 iunie 1931
            <p>oricine are o vacă. trebuie să renunțe la 1/2 lapte pentru ... [nu primește] bani ... sau [pierde] vaca. Trebuie să livreze lapte.</p>


